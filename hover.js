var img_1 = document.getElementById("img-1");


function mouseIn(ele) {
	if (ele == 1) {
		img_1.setAttribute("src", "https://upload.wikimedia.org/wikipedia/en/0/04/Facebook_f_logo_%282021%29.svg");
	}
}

function mouseOut(ele) {
	if (ele == 1) {
		img_1.setAttribute("src", "https://upload.wikimedia.org/wikipedia/en/0/04/Facebook_f_logo_%282021%29.svg");
    }
}